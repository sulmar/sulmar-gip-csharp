
int x = 10;
int y = 3;

Console.WriteLine(x + y);
Console.WriteLine(x - y);
Console.WriteLine(x * y);
Console.WriteLine(x / y);
Console.WriteLine(x % y); // modulo

// Increment
Console.WriteLine(++x);  
Console.WriteLine(x++);
Console.WriteLine(x);

// Decrement
Console.WriteLine(--x);
Console.WriteLine(x--);
Console.WriteLine(x);
